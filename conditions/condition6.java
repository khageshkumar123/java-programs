//program of if-else-if ladder condition
public class condition6
{
  public static void main(String[] args)
  {
    int marks=50;
    if(marks>85)
    {
      System.out.println("1st class");
    }
    else if(marks>75)
    {
      System.out.println("2nd class");
    }
    else
    {
      System.out.println("3rd class");
    }
  }
}