//program of switch case condition programme 
public class condition5
{
  public static void main(String[] args)
  {
    int x=10;
    switch(x)
    {
      case 1: 
      System.out.println("value is 10");
      break;
      case 2:
      System.out.println("value is 9");
      break;
      default:
      System.out.println("out of range");
    }
  }
}