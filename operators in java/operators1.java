//program on arithmetic operations in java
 class operators1
{
  public static void main(String[] args)
  {
    int a=10;
    int b=5;
    int add = a+b;
    int sub = a-b;
    int mul = a*b;
    int div = a/b;
    System.out.println("a+b= "+ add);
    System.out.println("a-b= "+ sub);
    System.out.println("a*b= "+ mul);
    System.out.println("a/b= "+ div);
  }
}